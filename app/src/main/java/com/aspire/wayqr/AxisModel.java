package com.aspire.wayqr;

public class AxisModel {

    private int _id;
    private String _location;
    private String _qr;
    private String _xAxis;
    private String _yAxis;
    private String _zAxis;

    public int get_id() {
        return _id;
    }

    public void set_id(int _id) {
        this._id = _id;
    }

    public String get_location() {
        return _location;
    }

    public void set_location(String _location) {
        this._location = _location;
    }

    public String get_qr() {
        return _qr;
    }

    public void set_qr(String _qr) {
        this._qr = _qr;
    }

    public String get_xAxis() {
        return _xAxis;
    }

    public void set_xAxis(String _xAxis) {
        this._xAxis = _xAxis;
    }

    public String get_yAxis() {
        return _yAxis;
    }

    public void set_yAxis(String _yAxis) {
        this._yAxis = _yAxis;
    }

    public String get_zAxis() {
        return _zAxis;
    }

    public void set_zAxis(String _zAxis) {
        this._zAxis = _zAxis;
    }
}
